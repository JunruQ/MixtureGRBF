def compute_data(obs, n_clusters, mean_x):
    clusters = obs['cluster_id'].values
    unique_clusters = np.unique(clusters)
    n_clusters_data = len(unique_clusters)
    print(f"检测到 {n_clusters_data} 个簇")
    
    # 验证簇数量是否匹配
    if n_clusters_data != n_clusters:
        print(f"警告：.h5ad 的簇数量 ({n_clusters_data}) 与 loom 的簇数量 ({n_clusters}) 不匹配，使用 .h5ad 的簇数量")
        mean_x = mean_x[:n_clusters_data, :]  # 截断 mean_x
        n_clusters = n_clusters_data
        global euclidean_ordering
        if euclidean_ordering is not None:
            euclidean_ordering = euclidean_ordering[:n_clusters_data]  # 截断 ordering
    
    # Number of cells
    n_cells_per_cluster = np.array([np.sum(clusters == c) for c in unique_clusters])
    
    # ROI groups
    regions = np.unique(obs['ROIGroup'])
    n_rois = len(regions)
    roidistro = np.zeros((n_rois, n_clusters))
    for i, r in enumerate(regions):
        for j, c in enumerate(unique_clusters):
            roidistro[i, j] = np.sum((obs['ROIGroup'] == r) & (clusters == c))
    roidistro = roidistro / roidistro.sum(axis=0, keepdims=True)
    
    # Superclasses
    if 'supercluster_term' in obs:
        superclass_data = obs['supercluster_term'].values
        superclass_assigned = []
        for c in unique_clusters:
            cluster_superclasses = superclass_data[clusters == c]
            most_common = pd.Series(cluster_superclasses).mode()[0] if len(cluster_superclasses) > 0 else 'Unknown'
            superclass_assigned.append(most_common)
        superclass_assigned = np.array(superclass_assigned)
    else:
        print("未找到 supercluster_term，使用原始 superclass 数组")
        superclass_assigned = superclass[:n_clusters]
    
    # 应用 euclidean_ordering（如果提供）
    # if euclidean_ordering is not None:
    #     if len(euclidean_ordering) != n_clusters:
