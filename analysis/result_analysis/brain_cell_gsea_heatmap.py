# 加载 loom 文件以获取 mean_x
def load_mean_x(loom_path):
    print("正在加载 loom 文件...")
    with loompy.connect(loom_path, "r") as ds:
        gene_names = ds.ra.Gene
        mean_x_full = ds[:, :].T  # 形状：(n_clusters, n_genes)
        # 定位目标基因
        gene_indices = [np.where(gene_names == g)[0][0] for g in genes if g in gene_names]
        valid_genes = [g for g in genes if g in gene_names]
        missing_genes = [g for g in genes if g not in gene_names]
        if missing_genes:
            print(f"警告：以下基因未在 loom 文件中找到，将忽略：{missing_genes}")
        # 初始化 mean_x
        n_clusters = mean_x_full.shape[0]
        mean_x = np.zeros((n_clusters, len(genes)))
        mean_x[:, :len(gene_indices)] = mean_x_full[:, gene_indices]
    return mean_x, n_clusters, valid_genes

# 加载并合并 .h5ad 文件的 obs（测试设置：2000 个细胞）
def load_and_merge_obs(neuron_path, non_neuron_path):
    print("正在加载 neuron.h5ad 的 obs...")
    obs_neuron = sc.read_h5ad(neuron_path, backed='r').obs
    print("正在加载 non_neuron.h5ad 的 obs...")
    obs_non_neuron = sc.read_h5ad(non_neuron_path, backed='r').obs
    print("正在合并 obs...")
    obs = pd.concat([obs_neuron, obs_non_neuron], axis=0)
    return obs

